package question;

public class 문제05번 {

	public static void main(String[] args) {

		int num1 = 10;

		// 10
		System.out.printf("%d \n", num1);
		num1++;

		// 11
		System.out.printf("%d \n", num1);
		++num1;

		// 12
		System.out.printf("%d \n", num1);
		--num1;

		// 11
		System.out.printf("%d \n", num1);
		num1--;

		// 10
		System.out.printf("%d \n", num1);
	}

}
