package question;

public class 문제03번 {

	public static void main(String[] args) {
		
		int num1, num2;
		num1 = 10 / 3;
		// '몫' 출력
		num2 = 10 % 3;
		// '나머지' 출력
		// 몫 : 3
		System.out.printf("      몫: %d \n", num1);
		
		// 나머지 : 1
		System.out.printf("나머지: %d \n", num2);
	}

}
